    public void getStock(int i,String str)
    {
        jPanel6.setVisible(true);
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn=DriverManager.getConnection("Jdbc:mysql://localhost:3307/shop","root","sandeep");
            if(i==1){
            PreparedStatement ps=conn.prepareStatement("select name,available from user where name=?");
            ps.setString(1,str);
            ResultSet rs=ps.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            }
            if(i==2)
            {  
               String symbol=str.substring(0,1);
                int count=Integer.parseInt(str.substring(1));
                PreparedStatement ps=conn.prepareStatement("select name,available from user where available"+symbol+"?");
                ps.setInt(1, count);
                  ResultSet rs=ps.executeQuery();
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                
            }
          
          
          else
            {
                PreparedStatement ps=conn.prepareStatement("select name,available from user where name like ?");
                ps.setString(1,str+"%");  
                      ResultSet rs=ps.executeQuery();
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));    
            }
        }
        catch(Exception e)
        {
            System.out.println(e); 
        }
    }
  

